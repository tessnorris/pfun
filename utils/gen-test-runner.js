#!/usr/bin/env node
// gen-test-runner.js
//
// Option 3: generate a test orchestrator by discovering *_test.pf files.
//
// This is the codegen endgame for the Pfun test story. It does NOT emit an
// in-Pfun aggregator that imports suite values (that hits V1's cross-module
// user-typed-value limitation — see NOTES.md). Instead it emits an ORCHESTRATOR
// that runs each self-contained test file as its own process and collects exit
// statuses. This:
//   - sidesteps both V1 limitations (cross-module resolution, fn-in-proc purity)
//   - gives per-file process isolation (a wedged/non-terminating test only hangs
//     its own process and is killed by a timeout, not the whole run)
//   - keeps every test file runnable by hand during development
//   - works unchanged from V1 (interpreter) to V2 (compiled): only the per-file
//     run command changes, driven by --mode.
//
// Each discovered file must be independently runnable (end by running its own
// suites — either `runSuites([...])` in a `proc main()` + `main();`, or the
// ad-hoc crypto_test style). The generator does not read file internals beyond
// discovery; it trusts the convention.
//
// Usage:
//   node gen-test-runner.js [--dir tests] [--mode interp|compile]
//                           [--pfun ./pfun.sh] [--timeout 60] [--out run-tests.sh]
//
// Convention: every file matching *_test.pf in --dir (non-recursive by default,
// --recursive to walk) is a runnable test. Helpers/fixtures must not use the
// _test.pf suffix.

const fs = require('fs');
const path = require('path');

function parseArgs(argv) {
  const args = {
    dir: 'tests',
    mode: 'interp',        // 'interp' (pfun <file>) or 'compile' (pfun -c then node)
    pfun: './pfun.sh',
    timeout: 60,           // seconds per test file; 0 disables
    out: 'run-tests.sh',
    recursive: false,
    runtime: '',   // path to pfun-runtime.js (compile mode: copied into each outdir/lib)
  };
  for (let i = 2; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--dir') args.dir = argv[++i];
    else if (a === '--mode') args.mode = argv[++i];
    else if (a === '--pfun') args.pfun = argv[++i];
    else if (a === '--timeout') args.timeout = parseInt(argv[++i], 10);
    else if (a === '--out') args.out = argv[++i];
    else if (a === '--runtime') args.runtime = argv[++i];
    else if (a === '--recursive') args.recursive = true;
    else if (a === '--help' || a === '-h') { printHelp(); process.exit(0); }
    else { console.error(`Unknown argument: ${a}`); process.exit(2); }
  }
  if (args.mode !== 'interp' && args.mode !== 'compile') {
    console.error(`--mode must be 'interp' or 'compile', got '${args.mode}'`);
    process.exit(2);
  }
  return args;
}

function printHelp() {
  console.log(`gen-test-runner.js — generate a Pfun test orchestrator

  --dir DIR         directory to scan for *_test.pf (default: tests)
  --mode MODE       interp | compile (default: interp)
  --pfun PATH       path to the pfun launcher (default: ./pfun.sh)
  --timeout SECS    per-file timeout in seconds, 0 to disable (default: 60)
  --out FILE        output orchestrator path (default: run-tests.sh)
  --recursive       scan subdirectories too
  -h, --help        this help`);
}

function discover(dir, recursive) {
  const found = [];
  const walk = (d) => {
    let entries;
    try { entries = fs.readdirSync(d, { withFileTypes: true }); }
    catch (e) { console.error(`Cannot read directory '${d}': ${e.message}`); process.exit(1); }
    for (const ent of entries) {
      const full = path.join(d, ent.name);
      if (ent.isDirectory()) { if (recursive) walk(full); }
      else if (ent.isFile() && ent.name.endsWith('_test.pf')) found.push(full);
    }
  };
  walk(dir);
  found.sort();
  return found;
}

function shQuote(s) { return "'" + String(s).replace(/'/g, `'\\''`) + "'"; }

function resolveLauncher(cmd, repoRoot) {
  return String(cmd).split(/\s+/).filter(Boolean).map(tok => {
    const looksPathy = tok.includes('/') || /\.(js|mjs|cjs|sh)$/.test(tok);
    if (!looksPathy) return tok;
    const abs = path.isAbsolute(tok) ? tok : path.resolve(tok);
    if (!fs.existsSync(abs)) return tok;
    return path.relative(repoRoot, abs);
  }).join(' ');
}

function generate(args, files, rootUp) {
  const repoRoot = process.cwd();
  const pfunCmd = resolveLauncher(args.pfun, repoRoot);
  const timeoutPrefix = args.timeout > 0 ? `timeout ${args.timeout} ` : '';
  const lines = [];
  lines.push('#!/usr/bin/env bash');
  lines.push('# GENERATED by gen-test-runner.js — do not edit by hand.');
  lines.push(`# mode=${args.mode}  files=${files.length}  generated=${new Date().toISOString()}`);
  lines.push('set -u');
  // Run from the project root: the compiler mirrors source paths into the
  // output dir, so compiling from a subdirectory makes "$PFUN_HOME/lib/..."
  // imports climb above the output root and write outside it.
  lines.push(`cd "$(dirname "\${BASH_SOURCE[0]}")/${rootUp}" || exit 2`);
  lines.push('PROJECT_ROOT="$(pwd)"');
  lines.push('export PFUN_HOME="${PFUN_HOME:-$PROJECT_ROOT}"');
  if (args.mode === 'compile') {
    lines.push(`export PFUN_RUNTIME=${args.runtime ? '"$PROJECT_ROOT/' + path.relative(repoRoot, path.resolve(args.runtime)) + '"' : '""'}`);
    const ioRel = args.runtime ? path.join(path.dirname(path.relative(repoRoot, path.resolve(args.runtime))), 'pfun-io.js') : '';
    lines.push(`export PFUN_IO=${ioRel ? '"$PROJECT_ROOT/' + ioRel + '"' : '""'}`);
  }
  lines.push('');
  lines.push('pass=0');
  lines.push('fail=0');
  lines.push('failed_files=()');
  lines.push('');
  lines.push('run_one() {');
  lines.push('  local f="$1"');
  lines.push('  echo');
  lines.push('  echo "=== $f ==="');
  if (args.mode === 'interp') {
    // pfun <file> runs the interpreter directly; exit code is the test result.
    lines.push(`  ${timeoutPrefix}${pfunCmd} "$f"`);
    lines.push('  local rc=$?');
  } else {
    // compile mode: pfun -c <file> -o <outdir>, then node the emitted JS.
    lines.push('  local base; base="$(basename "$f" .pf)"');
    lines.push('  local outdir; outdir="$(mktemp -d)"');
    lines.push(`  ${pfunCmd} -c "$f" -o "$outdir" || { echo "COMPILE FAILED: $f"; return 2; }`);
    lines.push('  local js; js="$(find "$outdir" -name "${base}.js" ! -path "*/lib/*" | head -1)"');
    lines.push('  if [ -z "$js" ]; then echo "NO OUTPUT JS: $f"; return 2; fi');
    lines.push('  # Ensure the runtime is available next to the emitted JS (compiler');
    lines.push('  # may not locate it to copy). Place it at both <outdir>/lib and');
    lines.push('  # alongside the JS\'s ./lib and ../lib to cover path variants.');
    lines.push('  local jsdir; jsdir="$(dirname "$js")"');
    lines.push('  if [ -n "$PFUN_RUNTIME" ]; then');
    lines.push('    mkdir -p "$jsdir/lib" "$outdir/lib"');
    lines.push('    cp "$PFUN_RUNTIME" "$jsdir/lib/pfun-runtime.js" 2>/dev/null || true');
    lines.push('    cp "$PFUN_RUNTIME" "$outdir/lib/pfun-runtime.js" 2>/dev/null || true');
    lines.push('    [ -n "$PFUN_IO" ] && { cp "$PFUN_IO" "$jsdir/lib/pfun-io.js" 2>/dev/null || true; cp "$PFUN_IO" "$outdir/lib/pfun-io.js" 2>/dev/null || true; }');
    lines.push('  fi');
    lines.push(`  ${timeoutPrefix}node "$js"`);
    lines.push('  local rc=$?');
    lines.push('  rm -rf "$outdir"');
  }
  lines.push('  if [ "$rc" -eq 0 ]; then');
  lines.push('    pass=$((pass+1))');
  lines.push('  else');
  lines.push('    fail=$((fail+1))');
  lines.push('    failed_files+=("$f (exit $rc)")');
  lines.push('  fi');
  lines.push('}');
  lines.push('');
  for (const f of files) lines.push(`run_one ${shQuote(f)}`);
  lines.push('');
  lines.push('echo');
  lines.push('echo "========================================"');
  lines.push('echo "Test files: $pass passed, $fail failed"');
  lines.push('if [ "$fail" -ne 0 ]; then');
  lines.push('  echo');
  lines.push('  echo "Failed:"');
  lines.push('  for ff in "${failed_files[@]}"; do echo "  - $ff"; done');
  lines.push('  exit 1');
  lines.push('fi');
  lines.push('echo "ALL TEST FILES PASSED"');
  lines.push('exit 0');
  lines.push('');
  return lines.join('\n');
}

function main() {
  const args = parseArgs(process.argv);
  const files = discover(args.dir, args.recursive);
  if (files.length === 0) {
    console.error(`No *_test.pf files found in '${args.dir}'${args.recursive ? ' (recursive)' : ''}.`);
    process.exit(1);
  }
  const relFiles = files.map(f => path.relative(process.cwd(), path.resolve(f)));
  const scriptDir = path.dirname(path.resolve(args.out));
  const rootUp = path.relative(scriptDir, process.cwd()) || '.';
  const script = generate(args, relFiles, rootUp);
  fs.writeFileSync(args.out, script, { mode: 0o755 });
  console.error(`Wrote ${args.out}: ${files.length} test file(s), mode=${args.mode}.`);
  for (const f of files) console.error(`  + ${f}`);
}

main();
